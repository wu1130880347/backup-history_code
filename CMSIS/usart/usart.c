void USART1_IRQHandler(void)                	     //串口1中断服务程序
{
  
          static u8 sum_f = 0;             //定义接收计数静态变量            

	if(USART_GetITStatus(USART1, USART_IT_RXNE) != RESET)  //验证接收状态
		{
                  Res = USART_ReceiveData(USART1)&0xff;	       //读取接收到的数据
                  rec[sum_f++] = Res;                          
                  if(rec[sum_f-1] == 0xA5)                      //接收帧校验
                  {
                    sum_f = 0;
                    LED0 ^= 1;                                 //下位机LED指示接收成功
                  }
                } 
        USART_ClearFlag(USART1,USART_IT_RXNE);               //清零接收标志位
        if(sum_f == 0 && rec[2] == 0x04)                     //接收到上位机设置参数命令
        {
          set_temper = (rec[3]<<8) + rec[4];   //上位机温度数据
          OLED_ShowString(48,4,"      ",8);
          OLED_ShowFloat(48,4,(float)set_temper*0.1,1,8);   //OLED显示上位机指令数据
          P = rec[5];                       //上位机P参数
          OLED_ShowString(24,5,"   ",8);
           OLED_ShowNum(24,5,P,0,8);     //显示上位机P参数
          while(num10<10)                //回复上位机
                usart2_send_char(rec[num10++]);
          num10 = 0;
          
        }
        else if(sum_f == 0 && (rec[2] == 0x01 || rec[2] == 0x00))   //接收到上位机请求连接，断开连接指令 
        {
          rec[3] = (u8)now_temp>>8;
          rec[4] = (u8)now_temp;
          rec[0] = 0xAA;
          rec[9] = 0xAA;
          while(num10<10)
                usart2_send_char(rec[num10++]);
          num10 = 0;
        }
        else if(sum_f == 0 && rec[2] == 0x02)  //接收到上位机开始运行指令   
        {
          rec[3] = (u8)now_temp>>8;
          rec[4] = (u8)now_temp;
          start = 1;
          while(num10<10)
                usart2_send_char(rec[num10++]);
          num10 = 0;
        }
        else if(sum_f == 0 && rec[2] == 0x03)   //接收到上位机结束运行指令
        {
          rec[3] = (u8)now_temp>>8;
          rec[4] = (u8)now_temp;
          start = 0;
          while(num10<10)
                usart2_send_char(rec[num10++]);
          num10 = 0;
        }
  

}

void usart2_send_char(u8 c)      //串口字节发送
{

	while(USART_GetFlagStatus(USART1,USART_FLAG_TC)==RESET); 
        USART_SendData(USART1,c);   

}
void usart2_send_string(u8 *data)        //串口字符串发送
{
	while(*(data))
          usart2_send_char(*(data++));	//发送数据到串口1
}


 



